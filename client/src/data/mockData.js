// ─────────────────────────────────────────────────────────────────────────────
// Emissions summary data per city (sectors + 5-year trend)
// ─────────────────────────────────────────────────────────────────────────────

const makeEmissions = (cityId, total, buildingPct, transportPct, industryPct, wastePct, baseYear2019) => {
  const buildings = Math.round(total * buildingPct);
  const transport = Math.round(total * transportPct);
  const industry = Math.round(total * industryPct);
  const waste = Math.round(total * wastePct);
  const other = total - buildings - transport - industry - waste;

  return {
    city_id: cityId,
    sectors: [
      { sector: 'Buildings', co2e_mt: buildings, pct: Math.round(buildingPct * 1000) / 10, color: '#FF6B6B' },
      { sector: 'Transport', co2e_mt: transport, pct: Math.round(transportPct * 1000) / 10, color: '#4ECDC4' },
      { sector: 'Industry', co2e_mt: industry, pct: Math.round(industryPct * 1000) / 10, color: '#45B7D1' },
      { sector: 'Waste', co2e_mt: waste, pct: Math.round(wastePct * 1000) / 10, color: '#96CEB4' },
      { sector: 'Other', co2e_mt: other, pct: Math.round(((other / total) * 1000)) / 10, color: '#FFEAA7' },
    ].filter(s => s.co2e_mt > 0),
    trend_5yr: [
      { year: 2019, co2e_mt: Math.round(baseYear2019) },
      { year: 2020, co2e_mt: Math.round(baseYear2019 * 0.93) },
      { year: 2021, co2e_mt: Math.round(baseYear2019 * 0.96) },
      { year: 2022, co2e_mt: Math.round(baseYear2019 * 0.97) },
      { year: 2023, co2e_mt: total },
    ],
  };
};

